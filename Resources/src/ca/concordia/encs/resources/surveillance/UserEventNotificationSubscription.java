package ca.concordia.encs.resources.surveillance;

import java.io.Serializable;

public class UserEventNotificationSubscription implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private DeviceEventNotificationInfo[] deviceEvents;
	private boolean recordingFlag;
	private String userId;
	private String notificationCallbackURL;

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getNotificationCallbackURL() {
		return notificationCallbackURL;
	}

	public void setNotificationCallbackURL(String notificationCallbackURL) {
		this.notificationCallbackURL = notificationCallbackURL;
	}

	public DeviceEventNotificationInfo[] getDeviceEvents() {
		return deviceEvents;
	}

	public void setDeviceEvents(DeviceEventNotificationInfo[] deviceEvents) {
		this.deviceEvents = deviceEvents;
	}

	public boolean isRecordingFlag() {
		return recordingFlag;
	}

	public void setRecordingFlag(boolean recordingFlag) {
		this.recordingFlag = recordingFlag;
	}

}
